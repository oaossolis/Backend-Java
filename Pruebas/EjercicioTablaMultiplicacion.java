public class EjercicioTablaMultiplicacion {
    public static void main(String[] args) {
        for(int contador =1; contador <=10; contador++){
            for(int mult =1; mult<=10; mult++){
                System.out.print(contador * mult);
                System.out.print(" ");
            }
            System.out.println();
        }      
    }
}
